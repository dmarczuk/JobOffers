package pl.joboffers.domain.offer;

import lombok.AllArgsConstructor;

@AllArgsConstructor
class Offer {

    private Integer id;
    private String url;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
